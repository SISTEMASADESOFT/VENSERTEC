       public List<CorreosDocumentosEnvios> F_CorreosDocumentosEnvios_Listar(int CodDocumentoVenta, int CodProforma)
       {

           try
           {
               List<CorreosDocumentosEnvios> Lista = new List<CorreosDocumentosEnvios>();
               DataTable dtDatos = obj.F_CorreosDocumentosEnvios_Listar(CodDocumentoVenta, CodProforma);

               foreach (DataRow r in dtDatos.Rows) {
                   Lista.Add(
                        new CorreosDocumentosEnvios() { 
                            id = Convert.ToInt32(r["id"].ToString()),
                            idTransaccion = r["idTransaccion"].ToString(),
                            CodUsuario = Convert.ToInt32(r["CodUsuario"].ToString()),
                            Usuario = Convert.ToString(r["Usuario"]),
                            Fecha = Convert.ToDateTime(r["Fecha"]),
                            FechaStr = Convert.ToDateTime(r["Fecha"]).ToString("dd/MM/yyyy"),
                            CodDocumentoVenta = Convert.ToInt32(r["CodDocumentoVenta"].ToString()),
                            CodProforma = Convert.ToInt32(r["CodProforma"].ToString()),
                            Email = Convert.ToString(r["Email"].ToString()),
                            Enviado = Convert.ToInt32(r["Enviado"].ToString()),
                            FechaEnvio = Convert.ToDateTime(r["FechaEnvio"]),
                            FechaEnvioStr = Convert.ToDateTime(r["FechaEnvio"]).ToString("dd/MM/yyyy"),
                            Cliente = Convert.ToString(r["Cliente"].ToString()),
                            NroRuc = Convert.ToString(r["NroRuc"].ToString()),
                            Factura = Convert.ToString(r["Factura"].ToString()),
                            Total = Convert.ToString(r["Total"].ToString()),
                            FechaEmision = Convert.ToString(r["FechaEmision"].ToString()),
                            TipoDoc = Convert.ToString(r["TipoDoc"].ToString()),
                            Empresa = Convert.ToString(r["Empresa"].ToString()),
                        });
               }
               return Lista;
           }
           catch (Exception ex)
           {

               throw ex;
           }

       }

